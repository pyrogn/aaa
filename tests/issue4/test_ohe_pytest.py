@pytest.mark.parametrize(
    ("input", "output"),
    [
        ("Moscow", [("Moscow", [1])]),
        (["Moscow"], [("Moscow", [1])]),
        ([1, 2, 3], [(1, [0, 0, 1]), (2, [0, 1, 0]), (3, [1, 0, 0])]),
        (input_for_big_test_matrix, big_test_matrix),
    ],
)
def test_ok(input, output):
    """Building OHE matrix"""
    assert fit_transform(input) == output


@pytest.mark.parametrize(
    "input",
    [
        (None,),
        (1, 2, 3),
        (lambda _: _),
    ],
)
def test_fail(input):
    """Fail on incorrect argument"""
    with pytest.raises(TypeError):  # error message is a bit different though
        fit_transform(*input)
